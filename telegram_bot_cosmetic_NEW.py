def load_token():
    env_token = os.getenv("BOT_TOKEN")
    if env_token:
        return env_token.strip()

    env_file = ".env.txt"
    if not os.path.exists(env_file):
        return ""

    with open(env_file, "r", encoding="utf-8") as file:
        for line in file:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            if "=" in line:
                key, value = line.split("=", 1)
                if key.strip() == "BOT_TOKEN":
                    return value.strip().strip('"').strip("'")
            return line.strip().strip('"').strip("'")

    return ""


def split_message(text):
    if len(text) <= MAX_MESSAGE_LENGTH:
        return [text]

    parts = []
    current = ""
    for paragraph in text.split("\n\n"):
        next_part = paragraph if not current else current + "\n\n" + paragraph
        if len(next_part) <= MAX_MESSAGE_LENGTH:
            current = next_part
            continue
        if current:
            parts.append(current)
        current = paragraph

    if current:
        parts.append(current)

    return parts


class TelegramBot:
    def __init__(self, token):
        self.api_url = f"https://api.telegram.org/bot{token}"
        self.offset = None
        self.sessions = {}

    def request(self, method, params=None):
        data = None
        if params:
            data = urllib.parse.urlencode(params).encode("utf-8")

        try:
            with urllib.request.urlopen(f"{self.api_url}/{method}", data=data, timeout=30) as response:
                payload = response.read().decode("utf-8")
                result = json.loads(payload)
        except urllib.error.HTTPError as error:
            payload = error.read().decode("utf-8", errors="replace")
            raise RuntimeError(f"Telegram API error in {method}: {payload}") from error

        if not result.get("ok"):
            raise RuntimeError(result)

        return result["result"]

    def send_message(self, chat_id, text, reply_markup=None):
        parts = split_message(text)
        for idx, part in enumerate(parts):
            params = {"chat_id": chat_id, "text": part, "parse_mode": "HTML"}
            if reply_markup and idx == len(parts) - 1:
                params["reply_markup"] = json.dumps(reply_markup, ensure_ascii=False)
            self.request("sendMessage", params)

    def edit_message(self, chat_id, message_id, text, reply_markup=None):
        params = {
            "chat_id": chat_id,
            "message_id": message_id,
            "text": text,
            "parse_mode": "HTML",
        }
        if reply_markup:
            params["reply_markup"] = json.dumps(reply_markup, ensure_ascii=False)
        self.request("editMessageText", params)

    def answer_callback(self, callback_query_id, text=None):
        params = {"callback_query_id": callback_query_id}
        if text:
            params["text"] = text
        try:
            self.request("answerCallbackQuery", params)
        except RuntimeError as error:
            print(error)

    def get_session_questions(self, session):
        return session["questions"]

    def build_question(self, question):
        return {
            "inline_keyboard": [
                [{"text": option, "callback_data": f"answer:{idx}"}]
                for idx, option in enumerate(question["options"])
            ]
        }

    def start_quiz(self, chat_id):
        questions = QUESTIONS.copy()
        random.shuffle(questions)
        self.sessions[chat_id] = {"question_idx": 0, "score": 0, "questions": questions}
        question = questions[0]
        self.send_message(
            chat_id,
            (
                f"<b>CosmeticBotTest</b>\n"
                f"Вопросы идут в случайном порядке.\n\n"
                f"<b>Вопрос 1/{len(questions)}</b>\n"
                f"{html.escape(question['question'])}"
            ),
            self.build_question(question),
        )

    def handle_message(self, message):
        chat = message.get("chat", {})
        chat_id = chat.get("id")
        text = (message.get("text") or "").strip()

        if not chat_id:
            return

        if text in ("/start", "start", "Start", "старт", "Старт", "Помощь"):
            self.send_message(chat_id, INTRO_TEXT, MAIN_KEYBOARD)
        elif text in ("/study", "Конспект", "конспект"):
            self.send_message(chat_id, STUDY_TEXT, MAIN_KEYBOARD)
        elif text in ("/test", "Начать тест", "начать тест"):
            self.start_quiz(chat_id)
        elif text in ("/cancel", "Отмена", "отмена"):
            self.sessions.pop(chat_id, None)
            self.send_message(chat_id, "Тест отменен.", MAIN_KEYBOARD)
        else:
            self.send_message(chat_id, "Выберите команду на клавиатуре или напишите /start.", MAIN_KEYBOARD)

    def handle_callback(self, callback):
        callback_id = callback["id"]
        message = callback.get("message", {})
        chat_id = message.get("chat", {}).get("id")
        message_id = message.get("message_id")
        data = callback.get("data", "")

        if not chat_id or not message_id:
            self.answer_callback(callback_id)
            return

        session = self.sessions.get(chat_id)
        if not session or not data.startswith("answer:"):
            self.answer_callback(callback_id, "Начните тест командой /test")
            return

        choice = int(data.split(":", 1)[1])
        question_idx = session["question_idx"]
        questions = self.get_session_questions(session)
        question = questions[question_idx]
        correct_idx = question["answer"]
        chosen_answer = question["options"][choice]
        correct_answer = question["options"][correct_idx]

        if choice == correct_idx:
            session["score"] += 1
            result_text = "✅ <b>Верно.</b>"
            self.answer_callback(callback_id, "Верно")
        else:
            result_text = "❌ <b>Неверно.</b>"
            self.answer_callback(callback_id, "Неверно")

        feedback_text = (
            f"{result_text}\n"
            f"Ваш ответ: {html.escape(chosen_answer)}\n"
            f"✅ Правильный ответ: <b>{html.escape(correct_answer)}</b>"
        )

        session["question_idx"] += 1
        if session["question_idx"] >= len(questions):
            score = session["score"]
            total = len(questions)
            self.sessions.pop(chat_id, None)
            self.edit_message(
                chat_id,
                message_id,
                (
                    f"{feedback_text}\n\n"
                    f"<b>Тест завершен.</b>\n"
                    f"Ваш результат: <b>{score}/{total}</b>."
                ),
            )
            self.send_message(chat_id, "Можно повторить тест или открыть конспект.", MAIN_KEYBOARD)
            return

        next_idx = session["question_idx"]
        self.edit_message(
            chat_id,
            message_id,
            (
                f"{feedback_text}\n\n"
                f"<b>Вопрос {next_idx + 1}/{len(questions)}</b>\n"
                f"{html.escape(questions[next_idx]['question'])}"
            ),
            self.build_question(questions[next_idx]),
        )

    def run(self):
        bot_info = self.request("getMe")
        print(f"Bot @{bot_info['username']} started. Press Ctrl+C to stop.")

        while True:
            updates = self.request(
                "getUpdates",
                {
                    "timeout": 25,
                    "offset": self.offset or "",
                    "allowed_updates": json.dumps(["message", "callback_query"]),
                },
            )

            for update in updates:
                self.offset = update["update_id"] + 1
                if "message" in update:
                    self.handle_message(update["message"])
                elif "callback_query" in update:
                    self.handle_callback(update["callback_query"])

            time.sleep(1)


def main():
    token = load_token()
    if not token:
        raise SystemExit(
            "Bot token not found. Put it into .env.txt as BOT_TOKEN=123456:ABC..."
        )

    TelegramBot(token).run()


if __name__ == "__main__":
    main()
